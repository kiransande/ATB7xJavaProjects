package ex_14072024;

public class Lab014 {
    public static void main(String[] args) {
        // Compound Assignment Operators
        int a = 10;
        // += , -=, /=, *= . %=
        // += ->
        a += 10; // a = a+10
        a -= 10; // a = a-10
        a /= 10; // a = a/10
        a *= 10; // a = a*10
    }
}